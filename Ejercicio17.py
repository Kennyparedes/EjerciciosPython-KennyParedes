#Ejercicio 17: Conversor de Millas a Kilómetros
#Escribe un programa que convierta una distancia en millas a kilómetros. Sabiendo que 1 milla equivale a 1.60934 kilómetros.
# Solicitar al usuario que ingrese la distancia en millas
millas = float(input("Por favor, ingresa la distancia en millas: "))

# Conversión de millas a kilómetros
kilometros = millas * 1.60934

# Mostrar el resultado
print(f"{millas} millas equivalen a {kilometros} kilómetros.")

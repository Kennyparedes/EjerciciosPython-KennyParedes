#Ejercicio 12: Calculadora de Área de un Rectángulo
# Crea un programa que calcule el área de un rectángulo. El usuario debe ingresar la longitud y el ancho del rectángulo.
# Solicitar la longitud y el ancho al usuario
longitud = float(input("Por favor, ingresa la longitud del rectángulo: "))
ancho = float(input("Por favor, ingresa el ancho del rectángulo: "))

# Calcular el área del rectángulo
area = longitud * ancho

# Mostrar el resultado
print(f"El área del rectángulo es: {area}")
